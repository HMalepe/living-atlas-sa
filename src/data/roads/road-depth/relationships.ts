export function getRelationshipsForRoad(roadSlug: string): RoadRelationship[] {
  return ROAD_DEPTH_RELATIONSHIPS.filter((rel) => rel.roadSlug === roadSlug);
}

export function getRelationshipBetween(
  slugA: string,
  slugB: string,
): RoadRelationship[] {
  return ROAD_DEPTH_RELATIONSHIPS.filter(
    (rel) =>
      (rel.roadSlug === slugA && rel.targetRoadSlug === slugB) ||
      (rel.roadSlug === slugB && rel.targetRoadSlug === slugA),
  );
}
