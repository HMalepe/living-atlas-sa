export function getTimelineForRoad(roadSlug: string): TimelineEvent[] {
  return ROAD_DEPTH_TIMELINE.filter((event) =>
    event.roadSlugs.includes(roadSlug),
  ).sort((a, b) => {
    const aDate = a.earliestDate ?? "";
    const bDate = b.earliestDate ?? "";
    if (aDate && bDate) return aDate.localeCompare(bDate);
    if (aDate) return -1;
    if (bDate) return 1;
    return 0;
  });
}
